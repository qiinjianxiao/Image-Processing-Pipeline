    tem(sth-line+1:sth+dh+line,stw+dw+1:stw+dw+line,3)=maxc;
else
    tem(sth-line+1:sth,stw-line+1:stw+dw+line,1)=maxc;
    tem(sth-line+1:sth,stw-line+1:stw+dw+line,2)=maxc;
    tem(sth-line+1:sth,stw-line+1:stw+dw+line,3)=maxc;
    tem(sth+dh+1:sth+dh+line,stw-line+1:stw+dw+line,1)=maxc;
    tem(sth+dh+1:sth+dh+line,stw-line+1:stw+dw+line,2)=maxc;
    tem(sth+dh+1:sth+dh+line,stw-line+1:stw+dw+line,3)=maxc;
